# Create A program that takes 2 numbers as input and prints whether the first number is greater than ,less than
# equal to the second number

First_Number = int(input("Enter First Number "))
Second_Number = int(input("Enter Second Number "))
print(' Is First_Number == Second_Number? ')
print(First_Number == Second_Number)
print("\nIs First_Number > Second_Number? (greater than)")
print(First_Number > Second_Number)

print("\nIs First_Number < Second_Number? (less than)")
print(First_Number < Second_Number)

print("\nIs First_Number >= Second_Number? (greater than or equal to)")
print(First_Number >= Second_Number)

print("\nIs First_Number <= Second_Number? (less than or equal to)")
print(First_Number <= Second_Number)