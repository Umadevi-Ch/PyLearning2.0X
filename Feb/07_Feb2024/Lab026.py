# Not Operator also called Negation ==Unary(Booleans)
is_married = True
print(not is_married)
# 'not' will do reverse of whatever the value it is and it will work only with booleans...

# is Operator --Identity Operator
a = 5
b = 5
print(a is b)

a = 5
b = 5
c = False
print(a is b)  # True
print(a is c)  # False

my_list = [1, 2, 3]
